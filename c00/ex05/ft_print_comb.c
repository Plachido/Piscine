#include <unistd.h>

void	ft_print_comb(void)
{
	int		a[3];
	int		i;
	int		j;
	char	num;

	i = 1;
	while (i <= 789)
	{
		a[2] = i % 10;
		a[1] = (i / 10) % 10;
		a[0] = (i / 100) % 100;
		if (a[2] > a[1] && a[1] > a[0])
		{
			j = 0;
			while (j < 3)
			{
				num = a[j] + '0';
				write(1, &num, 1);
				j++;
			}
			if (i != 789)
				write(1, ", ", 2);
		}
		i++;
	}
}
