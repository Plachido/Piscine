#include <unistd.h>

void	ft_putnbr(int nb)
{
	char show;

	if (nb == -2147483648)
		write(1, "-2147483648", 11);
	else if (nb < 0)
	{
		nb = -1 * nb;
		write(1, "-", 1);
	}
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else if (nb >= 0)
	{
		show = nb + '0';
		write(1, &show, 1);
	}
}
